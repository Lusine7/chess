static const int *PSTS[7] = {
    NULL, PST_PAWN, PST_KNIGHT, PST_BISHOP, PST_ROOK, PST_QUEEN, PST_KING
};

/* Returns score from WHITE's perspective */
int evaluate(Board *b) {
    int score = 0;
    for (int sq = 0; sq < 64; sq++) {
        int p = b->squares[sq];
        if (p == EMPTY) continue;
        int color  = p > 0 ? 1 : -1;
        int ap     = p < 0 ? -p : p;
        int pst_sq = (color == 1) ? sq : (7 - sq / 8) * 8 + sq % 8;
        score += color * (MATERIAL[ap] + PSTS[ap][pst_sq]);
    }
    return score;
}
